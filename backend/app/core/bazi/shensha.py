# 桃花 = 三合局第二字
# 华盖 = 三合局第三字（墓库）


def _ma(zhi: str) -> str:
    """驿马：三合第一字之冲。"""
    first = SAN_HE[zhi][0]
    return _CHONG[first]


def _taohua(zhi: str) -> str:
    """桃花：三合第二字。"""
    return SAN_HE[zhi][1]


def _huagai(zhi: str) -> str:
    """华盖：三合第三字（墓库）。"""
    return SAN_HE[zhi][2]


def find_shensha(chart: BaziChart) -> dict[str, list[str]]:
    """返回命盘中命主所带的神煞，按神煞名分组列出所在柱位。

    返回形如 {"天乙贵人": ["日柱"], "文昌": ["时柱"], ...}。
    查找范围：四柱天干地支。
    """
    day_master = chart.day_master
    # 年支与日支作为查神煞的基准
    year_zhi = chart.pillars["year"].zhi
    day_zhi = chart.pillars["day"].zhi

    # 各柱地支集合，用于判断"命中是否带某神煞"
    pillar_zhis = {k: chart.pillars[k].zhi for k in ("year", "month", "day", "hour")}
    PILLAR_LABEL = {"year": "年柱", "month": "月柱", "day": "日柱", "hour": "时柱"}

    result: dict[str, list[str]] = {}

    def _record(name: str, target_zhi: str) -> None:
        """记录某神煞落在哪些柱（地支等于 target_zhi 的柱）。"""
        hits = [PILLAR_LABEL[k] for k, z in pillar_zhis.items() if z == target_zhi]
        if hits:
            result.setdefault(name, []).extend(hits)

    # 以日干查
    for z in TIAN_YI.get(day_master, []):
        _record("天乙贵人", z)
    _record("文昌", WEN_CHANG.get(day_master, ""))
    _record("禄神", LU.get(day_master, ""))
    _record("羊刃", YANG_REN.get(day_master, ""))

    # 以年支与日支查（任一命中即可）
    for base_zhi in {year_zhi, day_zhi}:
        _record("驿马", _ma(base_zhi))
        _record("桃花", _taohua(base_zhi))
        _record("华盖", _huagai(base_zhi))

    return result


def find_shensha_with_desc(chart: BaziChart) -> dict[str, dict]:
    """返回命盘中命主所带的神煞，包含含义解释。

    返回形如 {"天乙贵人": {"positions": ["日柱"], "含义": "...", "特征": "...", "查法": "..."}, ...}。
    """
    raw = find_shensha(chart)
    result: dict[str, dict] = {}
    for name, positions in raw.items():
        desc = SHENSHA_DESC.get(name, {})
        result[name] = {
            "positions": positions,
            **desc,
        }
    return result
