def _build_index() -> dict[str, dict[str, GeoLocation]]:
    """构建 省 → 市 → GeoLocation 的二级索引（支持模糊匹配）。"""
    index: dict[str, dict[str, GeoLocation]] = {}
    for province, cities in _CITIES.items():
        bucket: dict[str, GeoLocation] = {}
        for name, lon, lat in cities:
            loc = GeoLocation(name=name, longitude=lon, latitude=lat,
                              full_name=f"{province}{name}")
            bucket[name] = loc
            # 去掉"市"字也作为 key（如"北京市"→"北京"）
            if name.endswith("市"):
                short = name[:-1]
                bucket.setdefault(short, loc)
        index[province] = bucket
        # 省会简称也作为省 key（如"北京市"→"北京"）
    return index


_INDEX = _build_index()


def list_provinces() -> list[str]:
    """所有省/直辖市（用于前端下拉）。"""
    return list(_INDEX.keys())


def list_cities(province: str) -> list[str]:
    """某省下属所有市名（去重，保留简称形式）。"""
    bucket = _INDEX.get(province, {})
    # 去掉"市"后缀重复，返回原名列表
    names = []
    seen = set()
    for name in bucket.keys():
        if name in seen:
            continue
        seen.add(name)
        names.append(name)
    return names


def find_location(province: str, city: str) -> GeoLocation | None:
    """按省+市查地理坐标。找不到返回 None。

    支持模糊：city 含"市"或不含都能匹配。
    """
    bucket = _INDEX.get(province)
    if bucket is None:
        return None
    # 精确匹配
    if city in bucket:
        return bucket[city]
    # 模糊：去掉/加上"市"
    for candidate in (city + "市", city.rstrip("市")):
        if candidate in bucket:
            return bucket[candidate]
    return None


def find_location_by_longitude(longitude: float) -> GeoLocation:
    """直接用经度构造一个 GeoLocation（用户手动输入经度时用）。"""
    return GeoLocation(
        name=f"东经{longitude}°",
        longitude=longitude,
        latitude=0.0,
        full_name=f"自定义 东经{longitude}°",
    )
