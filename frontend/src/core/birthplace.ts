// ---- 查找函数 ----

export function listProvinces(): string[] {
  const set = new Set<string>();
  Object.keys(DISTRICTS).forEach(p => set.add(p));
  Object.keys(CITIES).forEach(p => set.add(p));
  return Array.from(set).sort();
}

export function listCities(province: string): string[] {
  const names: string[] = [];
  if (DISTRICTS[province]) names.push(...Object.keys(DISTRICTS[province]));
  if (CITIES[province]) CITIES[province].forEach(([n]) => names.push(n));
  return names.sort();
}

export function listDistricts(province: string, city: string): { name: string; longitude: number }[] {
  const districts = DISTRICTS[province]?.[city];
  if (districts) {
    return districts.map(([name, lon]) => ({ name, longitude: lon }));
  }
  // 找不到区县数据 → 从 CITIES 表查市级经纬度作为唯一选项"市中心"
  const cityData = CITIES[province]?.find(([n]) => n === city);
  if (cityData) {
    return [{ name: "市中心", longitude: cityData[1] }];
  }
  return [];
}

export function findLocation(province: string, city: string, district?: string | null): { name: string; longitude: number; full_name: string } | null {
  let lon = 120;
  let name = city;
  let full = `${province}${city}`;

  if (district) {
    const districts = DISTRICTS[province]?.[city];
    if (districts) {
      const found = districts.find(([n]) => n === district);
      if (found) {
        name = found[0];
        lon = found[1];
        full = `${province}${city}${district}`;
        return { name, longitude: lon, full_name: full };
      }
    }
  }

  // 查 DISTRICTS 表（取第一个区作为城市默认经度）
  const dists = DISTRICTS[province]?.[city];
  if (dists && dists.length > 0) {
    lon = dists[0][1];
  } else {
    const cityData = CITIES[province]?.find(([n]) => n === city);
    if (cityData) lon = cityData[1];
  }
  full = `${province}${city}`;
  return { name, longitude: lon, full_name: full };
}

// 兼容 fetchLocations（给 client.ts 用）
import type { LocationItem, LocationsResponse } from '../api/types';
export function fetchLocationsLocal(): LocationsResponse {
  const provinces = listProvinces();
  const cities_map: Record<string, LocationItem[]> = {};
  for (const p of provinces) {
    cities_map[p] = listCities(p).map(name => {
      const loc = findLocation(p, name);
      return { name, longitude: loc?.longitude ?? 120, full_name: loc?.full_name ?? name };
    });
  }
  return { provinces, cities: cities_map };
}
