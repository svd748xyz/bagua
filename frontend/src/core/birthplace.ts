const _index: Record<string, Record<string, { name: string; longitude: number; full_name: string }>> = {};

for (const [province, cities] of Object.entries(CITIES)) {
  const bucket: Record<string, { name: string; longitude: number; full_name: string }> = {};
  for (const [name, lon] of cities) {
    const loc = { name, longitude: lon, full_name: `${province}${name}` };
    bucket[name] = loc;
    if (name.endsWith("市")) bucket[name.slice(0, -1)] = loc;
  }
  _index[province] = bucket;
}

export function listProvinces(): string[] {
  return Object.keys(_index);
}

export function listCities(province: string): string[] {
  return Object.keys(_index[province] ?? {});
}

export function findLocation(province: string, city: string): { name: string; longitude: number; full_name: string } | null {
  const bucket = _index[province];
  if (!bucket) return null;
  if (bucket[city]) return bucket[city];
  if (bucket[city + "市"]) return bucket[city + "市"];
  if (city.endsWith("市") && bucket[city.slice(0, -1)]) return bucket[city.slice(0, -1)];
  return null;
}

export function fetchLocationsLocal(): LocationsResponse {
  const provinces = listProvinces();
  const cities: Record<string, LocationItem[]> = {};
  for (const p of provinces) {
    cities[p] = listCities(p).map(name => {
      const loc = findLocation(p, name);
      return { name: loc?.name ?? name, longitude: loc?.longitude ?? 120, full_name: loc?.full_name ?? name };
    });
  }
  return { provinces, cities };
}
